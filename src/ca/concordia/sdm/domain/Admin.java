/**
 * This is the implementation of Admin domain class.
 * All attributes of Admin are declared; 
 * Attributes are defined and retrieved using setter getter methods respectively
 * @author Tonmoy Saha
 */


package ca.concordia.sdm.domain;



public class Admin {

	private int adminId;
	private String adminName;
	private String adminAddress;
	private String adminGender;
	private String adminBirthDate;
	private String adminEmail;
	private String adminHealthCardNo;
	private String adminPhone;
	public void setAdminId(int adminId) {
		this.adminId = adminId;
	}
	public int getAdminId() {
		return adminId;
	}
	public void setAdminName(String adminName) {
		this.adminName = adminName;
	}
	public String getAdminName() {
		return adminName;
	}
	public void setAdminAddress(String adminAddress) {
		this.adminAddress = adminAddress;
	}
	public String getAdminAddress() {
		return adminAddress;
	}
	public void setAdminGender(String adminGender) {
		this.adminGender = adminGender;
	}
	public String getAdminGender() {
		return adminGender;
	}
	public void setAdminBirthDate(String adminBirthDate) {
		this.adminBirthDate = adminBirthDate;
	}
	public String getAdminBirthDate() {
		return adminBirthDate;
	}
	public void setAdminEmail(String adminEmail) {
		this.adminEmail = adminEmail;
	}
	public String getAdminEmail() {
		return adminEmail;
	}
	public void setAdminHealthCardNo(String adminHealthCardNo) {
		this.adminHealthCardNo = adminHealthCardNo;
	}
	public String getAdminHealthCardNo() {
		return adminHealthCardNo;
	}
	public void setAdminPhone(String adminPhone) {
		this.adminPhone = adminPhone;
	}
	public String getAdminPhone() {
		return adminPhone;
	}
	
	
	
	
	
	
	

}
